#ifndef TOOL_TYPE_H
#define TOOL_TYPE_H

// Represents the types of the different Paint tools. Types may include
// Pencil, Bucket and Brush. If you make more tools like the Eraser or
// Stamp tools, you should add them here.
enum ToolType {
  kPencil = 0,
  kBucket,
  kBrush,
  kEraser,
};

#endif  // TOOL_TYPE_H
